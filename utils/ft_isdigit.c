#include "pushswap.h"

int	ft_isdigit(char *c)
{
	int	i;

	i = 0;
	if ((c[i] == '-' || c[i] == '+') && c[1])
		i++;
	while (c[i])
	{
		if (c[i] >= '0' && c[i] <= '9')
			i++;
		else
			return (0);
	}
	return (1);
}
